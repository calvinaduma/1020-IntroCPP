#include <iostream>
using namespace std;

int main(void) {
	cout << "This is a sentence." << endl;
	cout << "This is another sentence.";
	cout << endl << endl;

	cout << "This is a sentence. \n"; 
	cout << "This is another sentence.";
	cout << endl << endl;

	cout << "This is a sentence." << endl << 
		     "This is another sentence.";
	cout << endl << endl;

	return 0;
}


